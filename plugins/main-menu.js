global.style = async function style(text, style = 1) {
  const replacer = xStr.map((v, i) => ({
    original: v,
    convert: yStr[style]?.[i] || v,
  }));

  return text
    .toLowerCase()
    .split("")
    .map((char) => replacer.find((x) => x.original === char)?.convert || char)
    .join("");
};

